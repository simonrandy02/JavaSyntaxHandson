package Task1;

public class NamingConvention {
    public static void main(String[] args) {
        // Buat variabel dengan camelCase
        String namaLengkapMahasiswa = "Simon Randy Timoteus Sinaga";
        String nomorIndukMahasiswa = "2481039";
        double ipkSemesterLalu = 3.6;
        boolean statusAktifKuliah = true;
        String alamatRumah = "Jl. Letda Sujono, Tebing Tinggi";
        int jumlahSksYangDiambil = 20;

        // Tampilkan semua data
        System.out.println("=== DATA MAHASISWA ===");
        System.out.println("Nama Lengkap         : " + namaLengkapMahasiswa);
        System.out.println("Nomor Induk          : " + nomorIndukMahasiswa);
        System.out.println("IPK Semester Lalu    : " + ipkSemesterLalu);
        System.out.println("Status Aktif Kuliah  : " + statusAktifKuliah);
        System.out.println("Alamat Rumah         : " + alamatRumah);
        System.out.println("Jumlah SKS Diambil   : " + jumlahSksYangDiambil);
    }
}
